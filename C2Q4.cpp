//5. Average of values
#include <iostream>
using namespace std ;

int main ()
{
    double num1 = 28, num2 = 32, num3 = 37, num4 = 24, num5 = 33 ;
    double sum = num1 + num2 + num3 + num4 + num5 ;
    double avg = sum/5 ;

    cout << "The average of the values of 28, 32, 37, 24 & 33 is : " << avg ;
}